#variables
a = "tanisha" #double quotes gives default string
_b = 'sharma'  #counted as string
c = '''miss''' #still counted as string
d = 345       #by default it is determined as integer
e = 31.14     # by default float

#avoid reserved keywords to make variables
'''data types are as follows:
1. integers
2. floating points
3. strings- collection of characters in double, triple, single quotes
4. booleans- T/F
5. none '''

f = '''miss
tanisha
sharma''' #with triple quotes only, multiple lines can be printed
g = True 
h = None #used in function to return nothing, like void
H = 28 #variable names are case sensitive
#printing variables
print(a)
